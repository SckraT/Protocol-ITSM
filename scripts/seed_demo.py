# ── HTTP-хелперы ──────────────────────────────────────────────────────────

def api(method: str, path: str, body: dict | None = None, token: str | None = None) -> dict:
    """Выполнить HTTP-запрос к API; вернуть распарсенный JSON."""
    url = f"{BASE_URL}{path}"
    data = json.dumps(body).encode() if body is not None else None
    headers = {"Content-Type": "application/json", "Accept": "application/json"}
    if token:
        headers["Authorization"] = f"Bearer {token}"
    req = urllib.request.Request(url, data=data, headers=headers, method=method)
    try:
        with urllib.request.urlopen(req) as resp:
            return json.loads(resp.read())
    except urllib.error.HTTPError as exc:
        detail = exc.read().decode()
        raise RuntimeError(f"{method} {path} → {exc.code}: {detail}") from exc


def post(path: str, body: dict, token: str | None = None) -> dict:
    return api("POST", path, body, token)


def get(path: str, token: str) -> dict:
    return api("GET", path, token=token)


# ── Вспомогательная функция отображения прогресса ────────────────────────

def progress(current: int, total: int, label: str) -> None:
    bar_len = 30
    filled = int(bar_len * current / total)
    bar = "█" * filled + "░" * (bar_len - filled)
    print(f"\r  [{bar}] {current}/{total} {label}", end="", flush=True)


# ── Основной сценарий ────────────────────────────────────────────────────

def main() -> None:
    print("=== Seed демо-данных ===\n")

    # 1. Логин
    print("1. Авторизация…", end=" ")
    tokens = post("/auth/login", ADMIN_CREDENTIALS)
    token: str = tokens["access_token"]
    print("✓")

    # 2. Отделы
    print("2. Создание отделов…")
    dept_ids: list[int] = []
    for i, name in enumerate(DEPARTMENTS):
        result = post("/departments", {"name": name}, token)
        dept_ids.append(result["id"])
        progress(i + 1, len(DEPARTMENTS), name)
    print()

    # 3. Исполнители
    print("3. Создание исполнителей…")
    exec_ids: list[int] = []
    for i, (last, first, middle, dept_idx) in enumerate(EXECUTORS_DATA):
        full_name = f"{last} {first} {middle}"
        result = post("/executors", {"name": full_name, "department_id": dept_ids[dept_idx]}, token)
        exec_ids.append(result["id"])
        progress(i + 1, len(EXECUTORS_DATA), full_name)
    print()

    # 4. Пользователи
    print("4. Создание пользователей…")
    for i, (username, password, role, last_name, first_name, middle_name) in enumerate(USERS_DATA):
        post("/users", {
            "username": username,
            "password": password,
            "role": role,
            "last_name": last_name,
            "first_name": first_name,
            "middle_name": middle_name,
        }, token)
        progress(i + 1, len(USERS_DATA), username)
    print()

    # 5. Совещания
    print("5. Создание совещаний…")
    meeting_ids: list[int] = []
    for i, (title, participant_idxs) in enumerate(MEETINGS_DATA):
        meeting_date = d(-(i * 7 + 3))  # каждые 7 дней назад, начиная с 3 дней назад
        result = post("/meetings", {
            "title": title,
            "meeting_date": meeting_date,
            "description": f"Протокол совещания «{title}».",
            "participant_ids": [exec_ids[idx] for idx in participant_idxs],
        }, token)
        meeting_ids.append(result["id"])
        progress(i + 1, len(MEETINGS_DATA), title[:40])
    print()

    # 6. Задачи
    print("6. Создание задач…")
    ok_count = 0
    fail_count = 0
    for i, (topic, ticket, priority, state, due_offset, exec_idxs, meeting_idx, statuses) in enumerate(ITEMS_DATA):
        try:
            body: dict = {
                "topic": topic,
                "ticket": ticket,
                "priority": priority,
                "state": state,
                "due_date": d(due_offset),
                "executor_ids": [exec_ids[idx] for idx in exec_idxs],
            }
            if meeting_idx is not None:
                body["meeting_id"] = meeting_ids[meeting_idx]
            # Первый статус вместе с задачей
            if statuses:
                first_offset, first_note = statuses[0]
                body["status_date"] = d(first_offset)
                body["status_note"] = first_note

            result = post("/items", body, token)
            item_id = result["id"]

            # Дополнительные записи истории статусов
            for status_offset, note in statuses[1:]:
                post(f"/items/{item_id}/statuses", {
                    "status_date": d(status_offset),
                    "status_note": note,
                }, token)

            ok_count += 1
        except RuntimeError as exc:
            fail_count += 1
            print(f"\n  ⚠ {topic}: {exc}")
        progress(i + 1, len(ITEMS_DATA), f"ok={ok_count} err={fail_count}")
    print()

    # 7. Сводка
    print("\n=== Итог ===")
    print(f"  Отделов:      {len(dept_ids)}")
    print(f"  Исполнителей: {len(exec_ids)}")
    print(f"  Пользователей:{len(USERS_DATA)}")
    print(f"  Совещаний:    {len(meeting_ids)}")
    print(f"  Задач:        {ok_count} (ошибок: {fail_count})")

    if fail_count > 0:
        print("\nЕсть ошибки — проверьте вывод выше.")
        sys.exit(1)
    else:
        print("\nГотово. 🎉")


if __name__ == "__main__":
    main()
